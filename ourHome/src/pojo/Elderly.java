package pojo;

public class Elderly {

	private int id_e;
	private int id_u;
	private int age;
	private String reason;
	private int examine_e;
	private int estatus;
	public int getId_e() {
		return id_e;
	}
	public void setId_e(int id_e) {
		this.id_e = id_e;
	}
	public int getId_u() {
		return id_u;
	}
	public void setId_u(int id_u) {
		this.id_u = id_u;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public int getExamine_e() {
		return examine_e;
	}
	public void setExamine_e(int examine_e) {
		this.examine_e = examine_e;
	}
	public int getEstatus() {
		return estatus;
	}
	public void setEstatus(int estatus) {
		this.estatus = estatus;
	}
}
